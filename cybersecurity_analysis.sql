# Incident Response Time Analysis

SELECT responsible_team, AVG(response_time_minutes) AS avg_response_time
FROM incident_response
GROUP BY responsible_team
ORDER BY avg_response_time ASC;

#  Attacks Over Time (Last 6 Months)

SELECT DATE_FORMAT(timestamp, '%Y-%m') AS attack_month, COUNT(attack_id) AS total_attacks
FROM attack_logs
WHERE timestamp >= NOW() - INTERVAL 6 MONTH
GROUP BY attack_month
ORDER BY attack_month DESC;

#  Most Common Attack Types

SELECT attack_type, COUNT(attack_id) AS occurrence_count
FROM attack_logs
GROUP BY attack_type
ORDER BY occurrence_count DESC
LIMIT 10;

#  Users with the Highest Risk Level in Threat Intelligence

SELECT u.username, COUNT(ti.threat_id) AS high_risk_threats
FROM users u
JOIN threat_intelligence ti ON u.user_id = ti.user_id
WHERE ti.risk_level IN ('High', 'Critical')
GROUP BY u.username
ORDER BY high_risk_threats DESC;

#  Detect Possible Anomalies

SELECT attack_id, source_ip, destination_ip, attack_type, severity_level, firewall_action
FROM attack_logs
WHERE severity_level = 'Critical' AND firewall_action = 'Allowed';


#   Find Most Targeted Departments

SELECT u.department, COUNT(al.attack_id) AS attack_count
FROM users u
JOIN attack_logs al ON u.user_id = al.affected_user_id
GROUP BY u.department
ORDER BY attack_count DESC;

#  Top Responsible Teams Based on Incident Response

SELECT responsible_team, COUNT(response_id) AS incidents_handled, 
       AVG(response_time_minutes) AS avg_response_time
FROM incident_response
GROUP BY responsible_team
ORDER BY avg_response_time ASC;

#  users with flagged_incidents

SELECT u.username, COUNT(al.attack_id) AS flagged_incidents
FROM users u
JOIN attack_logs al ON u.user_id = al.affected_user_id
WHERE al.firewall_action = 'Flagged'
GROUP BY u.username
ORDER BY flagged_incidents DESC;

#  Correlation Between Attack Severity and Firewall Action

SELECT severity_level, firewall_action, COUNT(attack_id) AS count
FROM attack_logs
GROUP BY severity_level, firewall_action
ORDER BY FIELD(severity_level, 'Critical', 'High', 'Medium', 'Low');

#  Firewall Effectiveness Report

SELECT firewall_action, COUNT(*) AS action_count,
       ROUND((COUNT(*) * 100.0) / (SELECT COUNT(*) FROM attack_logs), 2) AS percentage
FROM attack_logs
GROUP BY firewall_action;

#  Incident Response for Critical Attacks

SELECT ir.response_id, al.attack_id, al.attack_type, al.severity_level, ir.response_action
FROM incident_response ir
JOIN attack_logs al ON ir.attack_id = al.attack_id
WHERE al.severity_level = 'Critical';

#   Find Devices with High-Risk Threat Intelligence

SELECT d.device_name, d.ip_address, ti.threat_name, ti.risk_level
FROM devices d
JOIN threat_intelligence ti ON d.device_id = ti.device_id
WHERE ti.risk_level IN ('High', 'Critical');




















