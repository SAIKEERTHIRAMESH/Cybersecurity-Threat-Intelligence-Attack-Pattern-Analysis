CREATE DATABASE Cyber_Security_2DB;
USE Cyber_Security_2DB;

CREATE TABLE users (
    user_id VARCHAR(50) PRIMARY KEY,
    username VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    department VARCHAR(50),
    access_level VARCHAR(20) CHECK (access_level IN ('Admin', 'User', 'Guest')),
    last_login TIMESTAMP
);

CREATE TABLE devices (  -- Changed from `device` to `devices`
    device_id VARCHAR(50) PRIMARY KEY,
    device_name VARCHAR(100) NOT NULL,
    ip_address VARCHAR(50) NOT NULL UNIQUE,
    os VARCHAR(50),
    location VARCHAR(100),
    user_id VARCHAR(50),
    FOREIGN KEY (user_id) REFERENCES users(user_id)
);

CREATE TABLE attack_types (
    attack_name VARCHAR(100) PRIMARY KEY,
    category VARCHAR(50),
    description TEXT
);

CREATE TABLE attack_logs (
    attack_id VARCHAR(50) PRIMARY KEY,
    timestamp DATETIME NOT NULL,
    source_ip VARCHAR(50) NOT NULL,
    destination_ip VARCHAR(50) NOT NULL,
    attack_type VARCHAR(100) NOT NULL,
    severity_level VARCHAR(20) CHECK (severity_level IN ('Low', 'Medium', 'High', 'Critical')),
    affected_user_id VARCHAR(50),
    affected_device_id VARCHAR(50),
    firewall_action VARCHAR(20) CHECK (firewall_action IN ('Blocked', 'Allowed', 'Flagged')),
    FOREIGN KEY (attack_type) REFERENCES attack_types(attack_name),
    FOREIGN KEY (affected_user_id) REFERENCES users(user_id),
    FOREIGN KEY (affected_device_id) REFERENCES devices(device_id) 
);

CREATE TABLE threat_intelligence (
    threat_id VARCHAR(50) PRIMARY KEY,
    threat_name VARCHAR(100) NOT NULL,
    attack_type VARCHAR(100) NOT NULL,
    risk_level ENUM('Low', 'Medium', 'High', 'Critical') NOT NULL,
    known_sources TEXT,
    user_id VARCHAR(50) NOT NULL,  -- Changed from INT to VARCHAR(50)
    device_id VARCHAR(50) NOT NULL, -- Changed from INT to VARCHAR(50)
    FOREIGN KEY (attack_type) REFERENCES attack_types(attack_name), -- Added reference to `attack_types`
    FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE,
    FOREIGN KEY (device_id) REFERENCES devices(device_id) ON DELETE CASCADE -- Fixed table reference
);


CREATE TABLE incident_response (
    response_id VARCHAR(50) PRIMARY KEY,
    timestamp DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    attack_id VARCHAR(50),
    attack_type VARCHAR(100) NOT NULL,
    response_action TEXT,
    response_time_minutes INT,
    responsible_team VARCHAR(100),
    FOREIGN KEY (attack_id) REFERENCES attack_logs(attack_id) ON DELETE CASCADE,
    FOREIGN KEY (attack_type) REFERENCES attack_types(attack_name) ON DELETE CASCADE
);


